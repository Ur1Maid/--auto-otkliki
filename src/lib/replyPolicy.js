// Чистая локальная классификация тредов чата: нужен ли вообще ответ.
// Без сети, без DeepSeek, без сайд-эффектов.
//
// Основная функция decideReply(messages) возвращает { action, reason }, где
// action ∈ { 'skip', 'needs_model', 'manual' }.
//
// Приоритет обработки (ранний возврат):
//   1. нет сообщений                         → skip
//   2. последнее сообщение — наше (applicant) → skip
//   3. нет входящих (employer) вообще         → skip
//   4. последнее входящее — системное/авто    → skip
//   5. последнее входящее содержит вопрос     → needs_model  (выше приглашения!)
//   6. последнее входящее — приглашение       → needs_model  (черновик ответа)
//   7. иначе                                  → needs_model  (черновик ответа)

import { matchesAnyPattern } from './answers.js';
import { normalizeText } from './knowledge.js';

// ---------------------------------------------------------------------------
// Паттерны системных / автоматических сообщений (seed-список по ограниченной
// выборке реального DOM; пополнять по мере появления новых примеров).
// ---------------------------------------------------------------------------
export const SYSTEM_AUTO_PATTERNS = [
  // Инфо-бабл про работодателя («У работодателя 2 отзыва и рейтинг 5.0»).
  // [^?]* — не перепрыгиваем через '?', чтобы реальный вопрос не утонул в system-skip.
  /у работодателя\b[^?]*отзыв/i,
  /рейтинг\s+\d/i,
  // Статусы доставки/просмотра отклика
  /ваш отклик (доставлен|просмотрен|отправлен)/i,
  /резюме (просмотрено|просмотрел)/i,
  /отклик на вакансию/i,
  // На случай если сопроводительное попадёт как текст
  /без сопроводительного письма/i,
  // Явные системные пометки
  /это автоматическое (сообщение|уведомление)/i,
  /не отвечайте на это сообщение/i,
];

// ---------------------------------------------------------------------------
// Паттерны приглашений на собеседование / в офис.
// ---------------------------------------------------------------------------
export const INVITATION_PATTERNS = [
  /приглаша(ем|ю)\b/i,
  /пригласить вас/i,
  /ждём вас/i,
  /на собеседование/i,
  /на интервью/i,
];

// ---------------------------------------------------------------------------
// Маркеры вопросов/просьб.
// Список расширяемый — ниже seed на основе типичных HR-запросов.
// ---------------------------------------------------------------------------
const QUESTION_MARKERS = [
  /\?/,                        // знак вопроса — главный сигнал
  /когда (вам )?удобно/i,
  /подскажите/i,
  /уточните/i,
  /сможете/i,
  /готовы ли/i,
  /напишите/i,
  /пришлите/i,
  /ваш телефон/i,
  /номер телефона/i,
  /во сколько/i,
  /как вас зовут/i,
  /расскажите/i,
  /есть ли у вас/i,
  /какой\b|какая\b|какие\b/i,
  /почему\b/i,
  /сколько\b/i,
  /где вы\b/i,
];

/**
 * Определяет, выглядит ли текст как вопрос или просьба предоставить информацию.
 *
 * @param {string} text
 * @returns {boolean}
 */
export function looksLikeQuestion(text) {
  if (typeof text !== 'string' || text.length === 0) return false;
  const normalized = normalizeText(text);
  return matchesAnyPattern(normalized, QUESTION_MARKERS);
}

/**
 * Локальная классификация треда: нужен ли авто-ответ.
 *
 * @param {Array<{ author: 'employer' | 'applicant', text: string, msgId?: string }>} messages
 * @returns {{ action: 'skip' | 'needs_model' | 'manual', reason: string }}
 */
export function decideReply(messages) {
  // 1. Нет сообщений
  if (!Array.isArray(messages) || messages.length === 0) {
    return { action: 'skip', reason: 'нет сообщений' };
  }

  // 2. Последнее сообщение — наше: мы уже написали, ждём работодателя.
  //    Опциональная цепочка терпит битый/null элемент (не роняет мульти-аккаунт прогон).
  const last = messages[messages.length - 1];
  if (last?.author === 'applicant') {
    return { action: 'skip', reason: 'последнее сообщение наше — ждём ответа работодателя' };
  }

  // 3. Нет ни одного входящего сообщения (пропускаем битые/не-объектные элементы)
  let lastEmployer = null;
  for (const msg of messages) {
    if (!msg || typeof msg !== 'object') continue;
    if (msg.author === 'employer') lastEmployer = msg;
  }
  if (lastEmployer === null) {
    return { action: 'skip', reason: 'нет входящих сообщений' };
  }

  const text = lastEmployer.text ?? '';

  // 4. Системное/автоматическое сообщение
  if (matchesAnyPattern(text, SYSTEM_AUTO_PATTERNS)) {
    return { action: 'skip', reason: 'системное/авто-сообщение' };
  }

  // 5. Вопрос/запрос информации — приоритет выше приглашения
  if (looksLikeQuestion(text)) {
    return { action: 'needs_model', reason: 'вопрос работодателя' };
  }

  // 6. Приглашение без вопроса
  if (matchesAnyPattern(text, INVITATION_PATTERNS)) {
    return { action: 'needs_model', reason: 'приглашение — черновик ответа' };
  }

  // 7. Неоднозначно — черновик ответа моделью
  return { action: 'needs_model', reason: 'неоднозначно — черновик ответа' };
}

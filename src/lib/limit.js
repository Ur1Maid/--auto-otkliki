// Чистый детектор «дневной лимит откликов достигнут» по видимому тексту страницы (M14.1).
// hh.ru блокирует отклики при достижении дневного лимита — показывает баннер/текст на
// странице отклика. Дальше откликаться бессмысленно → вызывающий код (review.js) делает
// graceful-стоп прогона и помечает heartbeat state='limit'.
//
// Без сайд-эффектов, без импорта Playwright. Текст страницы — untrusted (prompt-injection
// вектор): здесь он лишь матчится против фиксированных паттернов и НИКОГДА не становится
// селектором/командой; наружу ничего из текста не возвращается — только булев флаг.
//
// ЖИВАЯ ВЕРИФИКАЦИЯ: формулировки ниже — сид по типовым русским фразам hh.ru, но точные
// тексты дневного лимита нуждаются в подтверждении живым HTML от пользователя (follow-up).
// Паттерны легко дополнить новым регэкспом, как любые text-паттерны (см. playwright.md).
//
// Кириллица-safe: НЕ используем `\b`/`\w`-границы (на кириллице ломаются — урок M11.6
// runState.js). Якоря — пробелы/начало строки/окончания слов кириллицей.

import { matchesAnyPattern } from './answers.js';

/**
 * Текстовые признаки достижения дневного лимита откликов hh.ru (русский UI, case-insensitive).
 * Расширять этот массив новыми регэкспами, а не хардкодить проверки в вызывающем коде.
 * Формулировки — сид; нуждаются в живой верификации от пользователя (см. шапку файла).
 */
export const LIMIT_PATTERNS = [
  /откликнулись на максимальное количество вакансий/i,
  /лимит откликов исчерпан/i,
  /достигли лимита откликов/i,
  /сегодня больше нельзя откликаться/i,
  /исчерпан дневной лимит откликов/i,
  /превышен лимит откликов/i,
];

/**
 * Возвращает true, если текст страницы указывает, что дневной лимит откликов достигнут.
 * Никогда не бросает: не-строка/пустая строка/мусор → false.
 * @param {string} pageText — видимый текст страницы.
 * @returns {boolean}
 */
export function isLimitReached(pageText) {
  if (typeof pageText !== 'string' || pageText.length === 0) return false;
  return matchesAnyPattern(pageText, LIMIT_PATTERNS);
}
